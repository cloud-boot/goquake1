// Glyph returns the 8-byte bitmap for ch (bit 7 = leftmost pixel of
// the row) when ch is a printable ASCII code in 32..126, and the
// blank bitmap (all zeros) otherwise. Exported to support golden
// pixel tests + downstream callers that want to render a single
// glyph without materialising the whole sheet.
func Glyph(ch byte) [CellSize]byte {
	if ch < firstGlyph || ch >= firstGlyph+glyphCount {
		return [CellSize]byte{}
	}
	return font8x8[ch-firstGlyph]
}

// Build assembles the 16384-byte 128x128 conchars sheet. fgLow is the
// palette index used for "on" pixels in glyph cells 0..127; fgHigh is
// the palette index used in cells 128..255 (Quake's "yellow" duplicate
// half). Background pixels are zero so DrawCharacter treats them as
// transparent.
//
// Cell (col, row) covers pixels x in [col*8, col*8+8), y in [row*8,
// row*8+8). Glyph index ch maps to col=ch%16, row=ch/16. The font
// bitmap is repeated in both halves so the user sees readable text in
// either the white or the yellow text bank.
func Build(fgLow, fgHigh byte) []byte {
	const stride = GridDim * CellSize // 128 bytes per pixel row
	buf := make([]byte, SheetSize)
	for ch := 0; ch < GridDim*GridDim; ch++ {
		col := ch % GridDim
		row := ch / GridDim
		fg := fgLow
		var srcCh byte
		if ch < 128 {
			srcCh = byte(ch)
		} else {
			fg = fgHigh
			srcCh = byte(ch - 128)
		}
		bm := Glyph(srcCh)
		baseY := row * CellSize
		baseX := col * CellSize
		for py := 0; py < CellSize; py++ {
			rowBits := bm[py]
			rowOff := (baseY+py)*stride + baseX
			for px := 0; px < CellSize; px++ {
				if rowBits&(1<<(7-uint(px))) != 0 {
					buf[rowOff+px] = fg
				}
			}
		}
	}
	return buf
}
