// Package concharsfont builds the 128x128 Quake conchars sheet from an
// inlined 8x8 bitmap font, used when the asset pak does not ship a real
// gfx/conchars.lmp lump (Quake Remastered case: gfx.wad contains 163
// HUD/sbar lumps but no conchars). The synthetic fallback in the older
// code path filled each glyph cell with a single palette byte (a
// coloured square per glyph) -- legible-but-not-readable. This package
// emits real ASCII glyphs so menu titles, console text, centerprint
// strings, and the intermission scoreboard render as text the player
// can actually read.
//
// The conchars sheet layout matches Quake's: a 16x16 grid of 8x8
// glyph cells, total 128x128 = 16384 palette-indexed bytes. Glyph
// ch is at grid cell (ch%16, ch/16); each cell starts at
// (col*8, row*8) and runs 8 rows of 8 bytes.
//
// The glyph bitmaps below are hand-encoded (no external font asset)
// covering printable ASCII 32..126. Each glyph is 8 bytes; bit 7
// (mask 0x80) is the leftmost pixel of the row. ASCII 0..31 and
// 127..255 collapse to a blank glyph (all bits clear) -- DrawCharacter
// treats zeros as transparent so blanks are simply skipped at
// composite time.
//
// Quake renders the upper half of conchars (ch >= 128) as a "yellow"
// duplicate of the lower half. To match upstream semantics Build emits
// the same glyph set in 128..255 using a separate (typically brighter
// or yellow-tinted) palette index. Caller picks the foreground indices
// for both halves; common Quake values are 0xDC (white) and 0x67 / 0xFE
// (yellow).
package concharsfont

